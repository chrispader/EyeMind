import { BrowserWindow, app, session } from 'electron'
import electronSquirrelStartup from 'electron-squirrel-startup'
import { windowListeners } from '@listeners/window'
import {
  fixationFilterListeners,
  shutdownFixationFilterServer,
} from '@listeners/fixation-filter'
import { analysisListeners } from '@listeners/analysis'
import { fileSetupListener } from '@listeners/files-setup'
import { downloadListener } from '@listeners/download'
import { eyeTrackerListeners } from '@listeners/eye-tracker'
import { stateListeners } from '@listeners/state'
import { sessionListeners } from '@listeners/session'
import { testListeners } from '@listeners/test'

// This allows TypeScript to pick up the magic constants that's auto-generated by Forge's Webpack
// plugin that tells the Electron app where to look for the Webpack-bundled app code (depending on
// whether you're running in development or production).
declare const MAIN_WINDOW_WEBPACK_ENTRY: string
declare const MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY: string

// Handle creating/removing shortcuts on Windows when installing/uninstalling.
if (Boolean(electronSquirrelStartup)) app.quit()

const createMainWindow = (): BrowserWindow => {
  // Create the browser window.
  const mainWindow = new BrowserWindow({
    // fullscreen: true,
    // resizable: false,
    autoHideMenuBar: true,
    title: 'EyeMind',
    show: false,
    /// frame: false,
    height: 800,
    width: 1400,
    minWidth: 800,
    minHeight: 500,
    webPreferences: {
      preload: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
      // Allow loading files from local file system like images
      webSecurity: false,
      nodeIntegration: true,
    },
  })

  mainWindow.maximize()
  mainWindow.show()

  // used for the pop up windows
  mainWindow.webContents.setWindowOpenHandler(({ url }) => {
    if (url === 'about:blank') {
      return {
        action: 'allow',
        overrideBrowserWindowOptions: {
          frame: false,
          fullscreen: false,
          backgroundColor: '#E7EAED',
        },
      }
    }
    return { action: 'deny' }
  })

  // and load the index.html of the app.
  mainWindow.loadURL(MAIN_WINDOW_WEBPACK_ENTRY)

  // initiate set listeners
  stateListeners()

  // initiate window listeners
  windowListeners(mainWindow)

  // initiate fixation filter (R) listeners
  fixationFilterListeners(mainWindow)

  // initiate analysis listener
  analysisListeners(mainWindow)

  // initiate download listener
  downloadListener()

  // initiate file-setup listener
  fileSetupListener(mainWindow)

  // initiate eye-tracker listeners
  eyeTrackerListeners(mainWindow)

  // initiate session listeners
  sessionListeners()

  // initiate initiate test listeners
  testListeners()

  return mainWindow
}

// This method will be called when Electron has finished
// initialization and is ready to create browser windows.
// Some APIs can only be used after this event occurs.
app.on('ready', () => {
  session.defaultSession.allowNTLMCredentialsForDomains('*')

  let mainWindow = createMainWindow()

  const gotTheLock = app.requestSingleInstanceLock()

  if (!gotTheLock) {
    app.quit()
  } else {
    app.on('second-instance', () => {
      if (mainWindow.isMinimized()) mainWindow.restore()
      mainWindow.focus()
    })

    app.on('ready', () => {
      mainWindow = createMainWindow()
    })
  }
})

// Quit when all windows are closed, except on macOS. There, it's common
// for applications and their menu bar to stay active until the user quits
// explicitly with Cmd + Q.
app.on('window-all-closed', async () => {
  await shutdownFixationFilterServer()

  // quit electron app
  if (process.platform !== 'darwin') app.quit()
})

app.on('activate', () => {
  // On OS X it's common to re-create a window in the app when the
  // dock icon is clicked and there are no other windows open.
  if (BrowserWindow.getAllWindows().length === 0) createMainWindow()
})
